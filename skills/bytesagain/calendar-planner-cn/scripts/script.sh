cmd_faq() { cat << 'EOF'
# 中国日历 — 常见问题

Q: 为什么春节每年公历日期不同？
A: 春节是农历正月初一。农历月份基于月亮周期（29.5天），
   12个农历月只有354天，比公历年（365天）少11天。
   通过闰月调整，春节在公历1月21日到2月20日之间波动。
   最早春节：1月21日（如1966年）
   最晚春节：2月20日（如1985年）

Q: 闰月是怎么决定的？
A: 19年中加7个闰月（梅冬章，Metonic cycle）。
   具体哪个月是闰月：两个冬至之间，第一个没有中气的月份。
   中气 = 节气中的偶数项（雨水、春分、谷雨等12个）。
   闰月保留前一个月的名称，如"闰四月"。
   2023年有闰二月，2025年有闰六月。

Q: 二十四节气和农业有什么关系？
A: 节气反映太阳位置，直接关系到气温和日照变化：
   惊蛰（3月）：地温升高，虫蛰苏醒 → 开始春耕
   清明（4月）：气温稳定 → 播种期
   芒种（6月）：麦子成熟，稻子播种 → 抢收抢种
   小暑/大暑（7月）：最热 → 灌溉防旱
   白露/秋分（9月）：气温降 → 秋收
   霜降（10月）：初霜 → 冬储准备
   节气是中国农耕文明的精确时间框架。

Q: 农历有"十三月"吗？
A: 有，就是闰月。有闰月的年份有13个月，383-385天。
   平均每2.7年出现一次闰月。
   闰月不独立命名，而是叫"闰X月"（如闰四月）。

Q: 为什么有些年份没有大年三十？
A: 农历月份按实际朔日确定。如果腊月是小月（29天），
   则没有三十，二十九即为除夕。这不算异常，大约平均
   每隔一两年就会出现一次。除夕仍然是农历最后一天。
EOF
}

