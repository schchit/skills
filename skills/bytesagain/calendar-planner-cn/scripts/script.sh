## 伪科学黄历辨识
  传统黄历中不可信的内容：
    - "宜嫁娶/忌出行"等吉凶判断 — 无科学依据
    - "冲煞"方位 — 基于迷信而非天文
    - "胎神"占方 — 毫无医学根据
    - "紫白飞星" — 风水术数，非天文学

  可信的内容：
    - 朔望日期（初一、十五）— 天文计算结果
    - 节气日期和时刻 — 太阳位置的客观测量
    - 干支纪日 — 连续纪日系统，无中断记录
    - 月相（新月、满月、上弦、下弦）— 天文观测

## 历史日期核验
  古代历法多次改历，不同朝代使用不同历法
  陈久金《中国历法与数学》：权威历法史参考
  注意：秦汉以十月为岁首，建正不同影响月份对应
  儒略日（JD）是跨历法的统一时间标尺
