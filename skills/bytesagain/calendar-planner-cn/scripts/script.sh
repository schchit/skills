cmd_intro() { cat << 'EOF'
# 中国日历与节气 — 概述

## 阴阳合历体系
中国农历是一种阴阳合历（lunisolar calendar）：
  阴历部分：月份以朔望月为基础（新月到新月 = 29.53天）
  阳历部分：年份以回归年为基础（太阳回到春分点 = 365.24天）
  调和方法：设置闰月（19年7闰），使农历年平均长度接近回归年

## 基本周期
  朔望月：29.530588天（月亮圆缺一次）
  回归年：365.2422天（太阳南北回归一次）
  12个朔望月 = 354.37天（比回归年少约11天）
  所以每2-3年必须加1个闰月来追赶太阳

## 二十四节气
  节气按太阳黄经每隔15°划分（不是等时间间隔）
  太阳黄经0° = 春分，90° = 夏至，180° = 秋分，270° = 冬至
  定气法（现行）：按太阳实际位置计算（1645年起）
  平气法（古代）：把回归年等分24份

  节气分两类：
    节（12个）：立春、惊蛰、清明、立夏、芒种、小暑、立秋、白露、寒露、立冬、大雪、小寒
    气（12个）：雨水、春分、谷雨、小满、夏至、大暑、处暑、秋分、霜降、小雪、冬至、大寒

## 天干地支纪年
  天干（10个）：甲乙丙丁戊己庚辛壬癸
  地支（12个）：子丑寅卯辰巳午未申酉戌亥
  干支循环：60年一轮（甲子→乙丑→...→癸亥）
  2024年 = 甲辰年（龙年），2025 = 乙巳（蛇），2026 = 丙午（马）

## 生肖
  子鼠、丑牛、寅虎、卯兔、辰龙、巳蛇、午马、未羊、申猴、酉鸡、戌狗、亥猪
  生肖以立春为界（非春节），争议存在
EOF
}

cmd_standards() { cat << 'EOF'
# 农历计算标准

## 紫金山天文台标准
  中国科学院紫金山天文台负责官方农历编算
  依据：《农历编算法》国家标准（征求意见中）
  每年出版《中国天文年历》，含精确朔望和节气时刻

## 定气法计算
  太阳黄经：用天文算法精确计算太阳在黄道上的位置
  每隔15°为一个节气，共24个
  由于地球公转速度不均匀（近日点快、远日点慢），节气间隔不等：
    冬至到小寒：约14.7天
    夏至到小暑：约15.7天
  精确计算需要考虑：岁差、光行差、章动

## 朔日（新月）计算
  朔：月球黄经 = 太阳黄经的时刻
  北京时间（UTC+8）计算，朔时刻在哪天，哪天就是初一
  1929年起统一用东经120°标准时
  之前各朝代用不同京师时间（长安、开封、北京）

## 闰月规则
  1. 冬至必须在十一月
  2. 两个冬至之间如果有13个月，就有一个闰月
  3. 闰月是两个冬至之间第一个没有中气的月份
  4. 中气 = 24节气中的偶数个（雨水、春分、谷雨...）

## 历法精度
  现代农历精度：朔时刻精确到秒级
  古代：误差可达数小时到一天
  寿星天文历算法：独立计算，精度达到1分钟以内
  NASA JPL星历表（DE440）：提供高精度行星位置数据
EOF
}

cmd_troubleshooting() { cat << 'EOF'
# 农历常见问题

## 农历转换错误
  问题：不同万年历APP给出不同农历日期
  原因：
    - 算法精度不同（平气法vs定气法，旧算法vs天文算法）
    - 时区处理不一致（有的用UTC，有的用UTC+8）
    - 闰月判定在边界情况下可能不同
  解决：以紫金山天文台《中国天文年历》为准
  可靠工具：寿星天文历（网页版），香港天文台农历转换

## 闰月判定争议
  规则看似简单，但边界情况有争议：
  2033年问题：按规则可能出现闰冬月（历史罕见）
  实际处理：紫金山天文台最终裁定，不同软件可能给不同结果
  建议：对远期日期（>50年）的农历转换保持谨慎

## 干支纪年转换错误
  常见错误：用公元年直接算干支
  正确方法：
    天干 = (公元年 - 3) mod 10  → 对应甲乙丙丁戊己庚辛壬癸
    地支 = (公元年 - 3) mod 12  → 对应子丑寅卯辰巳午未申酉戌亥
  注意：干支年以立春为界，不是春节也不是元旦
  例：2026年2月4日立春后才算丙午年

## 节气日期每年不同
  原因：回归年 ≠ 日历年（365天 vs 365.2422天）
  规律：
    - 同一节气在公历中前后波动1-2天
    - 闰年会使节气日期提前1天
    - 清明：通常4月4日或5日
    - 冬至：通常12月21日或22日
  注意：节气时刻精确到分钟，跨日时需注意时区
EOF
}

cmd_performance() { cat << 'EOF'
# 农历计算技巧

## 寿星天文历万年历算法
  由许剑伟开发，精度达到1分钟以内
  核心：VSOP87理论（行星运动），ELP2000（月球运动）
  可计算范围：前722年到公元9999年
  开源实现：JavaScript版本可在浏览器运行
  关键函数：
    - 朔日计算（精确新月时刻）
    - 节气计算（太阳黄经精确值）
    - 干支推算（年月日时四柱）
  地址：www.bintrue.net/lifa/lifa.html

## 快速干支推算口诀

### 年干支速算
  记住基准：2024 = 甲辰
  天干10年一轮：2024甲 → 2025乙 → 2026丙 → ...
  地支12年一轮：2024辰 → 2025巳 → 2026午 → ...

### 月干支速算
  正月天干 = 年干×2 + 1（取模10）
  例：丙午年 → 丙=3 → 3×2+1=7=庚 → 正月为庚寅

### 日干支速算
  需要查表或用公式（最复杂）
  近似法：记住每月1号的干支，加天数推算
  推荐直接用万年历工具查询

## 批量日期转换
  需求：将一批公历日期转为农历
  方法：
    1. 预计算朔日表（每个月新月时刻），存入数组
    2. 对每个公历日期，二分查找落在哪个农历月
    3. 日期差即为农历日
  优化：缓存100年朔日表（约1200条），查询O(log n)
EOF
}

